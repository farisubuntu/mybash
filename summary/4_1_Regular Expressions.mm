  <body>
    <p>
      A bracket expression is a list of characters enclosed by &quot;<font color="#ff0000">[</font>&quot; and &quot;<font color="#ff0000">]</font>&quot;. It matches any single character in that list; if the first character of the list is the caret, &quot;<font color="#ff0000">^</font>&quot;, then it matches any character NOT in the list. For example, the regular expression &quot;[0123456789]&quot; matches any single digit.
    </p>
    <p>
      
    </p>
    <p>
      Within a bracket expression, a range expression consists of two characters separated by a hyphen. It matches any single character that sorts between the two characters.
    </p>
  </body>
